wavelength = int(input())
if wavelength >= 620:
    print('Red')
elif wavelength >= 590:
    print('Orange')
elif wavelength >= 570:
    print('Yellow')
elif wavelength >= 495:
    print('Green')
elif wavelength >= 450:
    print('Blue')
elif wavelength >= 425:
    print('Indigo')
elif wavelength >= 380:
    print('Violet')